(function(){
  const m = window.POSITION_NAME_TO_SLUG;
  let added = 0; const missing = [];
  for (const alias in window.POSITION_ALIASES){
    const full = window.POSITION_ALIASES[alias];
    const slug = m.get(full);
    if (!slug){ missing.push(alias + "→" + full); continue; }
    if (!m.has(alias)) { m.set(alias, slug); added++; }
  }
  if (missing.length) console.warn("[選佛譜] 別名全名未匹配（請查全名拼寫）：", missing);
  console.info("[選佛譜] 位次別名已載入 " + added + " 條");
})();

