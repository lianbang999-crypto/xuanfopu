// 校訂進度統計
window.PROGRESS = (function(){
  const total = window.POSITIONS.length;
  const refined = window.POSITIONS.filter(p => p.refined).length;
  // 按門統計
  const byDoor = new Map();
  for (const p of window.POSITIONS){
    const cur = byDoor.get(p.door) || { refined:0, total:0 };
    cur.total++;
    if (p.refined) cur.refined++;
    byDoor.set(p.door, cur);
  }
  return { total, refined, byDoor };
})();
